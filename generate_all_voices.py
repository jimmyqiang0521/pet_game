def generate_md5(text):
    """
    生成文本的MD5哈希值
    """
    hash_obj = hashlib.md5(text.encode('utf-8'))
    return hash_obj.hexdigest()

async def generate_voice(text, output_file, voice="zh-CN-XiaoxiaoNeural", rate="-20%"):
    """
    使用edge-tts生成语音
    """
    communicate = edge_tts.Communicate(text, voice, rate=rate)
    await communicate.save(output_file)
    print(f"生成完成: {output_file}")

async def main():
    """
    生成所有语音文件
    """
    # 创建音频目录
    audio_dir = "audio"
    if not os.path.exists(audio_dir):
        os.makedirs(audio_dir)
    
    # 统计信息
    total_messages = 0
    generated_files = 0
    
    print("开始生成所有语音文件...")
    print(f"语音模型: zh-CN-XiaoxiaoNeural (女幼童声音)")
    print(f"语速: -20% (更慢)")
    print("=" * 60)
    
    # 遍历所有消息
    for pet_type, categories in MESSAGES.items():
        print(f"\n宠物类型: {pet_type}")
        for category, messages in categories.items():
            print(f"  类别: {category}")
            for text in messages:
                total_messages += 1
                # 生成MD5文件名
                md5_hash = generate_md5(text)
                filename = f"{md5_hash}.mp3"
                output_path = os.path.join(audio_dir, filename)
                
                # 检查文件是否已存在
                if os.path.exists(output_path):
                    print(f"    跳过: {text} (文件已存在)")
                    continue
                
                # 生成语音
                try:
                    await generate_voice(text, output_path)
                    generated_files += 1
                except Exception as e:
                    print(f"    失败: {text} - {e}")
    
    print("=" * 60)
    print(f"生成完成！")
    print(f"总消息数: {total_messages}")
    print(f"生成文件数: {generated_files}")
    print(f"跳过文件数: {total_messages - generated_files}")

if __name__ == "__main__":
    asyncio.run(main())
