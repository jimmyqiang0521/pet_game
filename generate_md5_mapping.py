# 生成MD5映射
def generate_md5_mapping():
    md5_mapping = {}
    
    for pet_type, categories in MESSAGES.items():
        for category, messages in categories.items():
            for text in messages:
                hash_obj = hashlib.md5(text.encode('utf-8'))
                md5_mapping[text] = hash_obj.hexdigest()
    
    return md5_mapping

# 生成JavaScript代码
def generate_js_code(md5_mapping):
    js_code = "// 同步版本的MD5函数\nfunction md5Sync(str) {\n    // 简化的MD5实现，用于生成与Python一致的哈希\n    const md5Hash = {\n"
    
    for text, md5_hash in md5_mapping.items():
        # 转义特殊字符
        escaped_text = text.replace('\\', '\\\\').replace('"', '\\"').replace('\n', '\\n')
        js_code += f"        '{escaped_text}': '{md5_hash}',\n"
    
    js_code += "    };\n    return md5Hash[str] || '00000000000000000000000000000000';\n}"
    
    return js_code

